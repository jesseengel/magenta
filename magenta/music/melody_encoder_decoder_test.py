class OneHotMelodyEncoderDecoderTest(tf.test.TestCase):

  def setUp(self):
    self.med = melody_encoder_decoder.OneHotMelodyEncoderDecoder(60, 72, 0)

  def testMinNoteMaxNoteAndTransposeToKeyValidValues(self):
    # Valid parameters
    melody_encoder_decoder.OneHotMelodyEncoderDecoder(0, 128, 0)
    melody_encoder_decoder.OneHotMelodyEncoderDecoder(60, 72, 11)

    # Invalid parameters
    self.assertRaises(
        ValueError,
        melody_encoder_decoder.OneHotMelodyEncoderDecoder, -1, 72, 0)
    self.assertRaises(
        ValueError,
        melody_encoder_decoder.OneHotMelodyEncoderDecoder, 60, 129, 0)
    self.assertRaises(
        ValueError,
        melody_encoder_decoder.OneHotMelodyEncoderDecoder, 60, 71, 0)
    self.assertRaises(
        ValueError,
        melody_encoder_decoder.OneHotMelodyEncoderDecoder, 60, 72, -1)
    self.assertRaises(
        ValueError,
        melody_encoder_decoder.OneHotMelodyEncoderDecoder, 60, 72, 12)

  def testInitValues(self):
    self.assertEqual(self.med.min_note, 60)
    self.assertEqual(self.med.max_note, 72)
    self.assertEqual(self.med.transpose_to_key, 0)
    self.assertEqual(self.med.input_size, 14)
    self.assertEqual(self.med.num_classes, 14)
    self.assertEqual(self.med.no_event_label, 0)

